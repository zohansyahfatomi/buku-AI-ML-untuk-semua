import numpy as np

p = np.arange(1, 6)
print("array p : ",p)

logp = np.log(p)
print("log(p) : ",logp)

sqrtp = np.sqrt(p)
print("sqrt(p) : ",sqrtp)

sinp = np.sin(p)
print("sin(p) : ",sinp)

cosp = np.cos(p)
print("cos(p) : ",cosp)

tanp = np.tan(p)
print("tan(p) : ",tanp)

expp = np.exp(p)
print("exp(p) : ",expp)



