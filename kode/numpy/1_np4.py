import numpy as np

print("np.zeros((2, 3)) : ",np.zeros((2, 3)))
print("np.ones((3, 4)) : ",np.ones((3, 4)))
print("np.empty((2, 3)) : ",np.empty((2, 3)))
print("np.full((2, 2), 5) : ",np.full((2, 2), 5))
print("np.eye(3, 3) : ", np.eye(3, 3))
print("np.random.random((2,2)) : ",np.random.random((2,2)))
print("np.linspace(0, 2, 9) : ",np.linspace(0, 2, 9))
