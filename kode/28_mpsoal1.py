import matplotlib.pyplot as plt
# x axis values
x = [1,2,3]
# y axis values
y = [3,5,2]
# Plot lines and/or markers to the Axes.
plt.plot(x, y,'k')
# Set the x axis label of the current axis.
plt.xlabel('Sumbu-X')
# Set the y axis label of the current axis.
plt.ylabel('Sumbu-Y')
# Set a title
plt.title('Nama_Anda')
# Display a figure.
plt.show()
